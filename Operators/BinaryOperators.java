
public class BinaryOperators{
    public static void main (String[] args){
        int A = 10;
        int B = 5;
        System.out.println("add = "+(A+B));
        System.out.println("Subtract = "+(A-B));
        System.out.println("Divide = "+(A/B));
        System.out.println("Remainder (modulo) = "+(A%B));

    }
}